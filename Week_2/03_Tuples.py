# () Tuples
# Just like lists, Tuples are containers that can store any kind of dataTypes
# Tuples are immutable; They can't be modified
# They are ordered

# Empty Tuple
my_tuple = ()
print(my_tuple)
print(type(my_tuple))

# Tuple with integers
my_tuple_1 = (1, 2, 3, 4)

# Tuple with mixed DataTypes
my_tuple_2 = (1, "Hello", 3.14, False, ['A', 'B', 'C'])

# Nested Tuple
my_tuple_3 = ('Lion', (1, 2, 3, 4))

my_tuple_4 = (2.3)
print(type(my_tuple_4))

my_tuple_5 = ("Hello",)
print(type(my_tuple_5))
