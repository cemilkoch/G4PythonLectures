# Python supports integers, floating point number
# They are defined as int, float

# Basic Arithmetic

# Addition
print(30 + 50)

# Subtraction
print(50 - 25)

# Multiplication
print(20 * 5)

# Division
print(100 / 5)
print(65 / 14)

# Floor Division //
print(100 // 5)
print(65 // 14)

# Modulo % modulo operator will return the remainder
print(7 % 4)

# Powers ** to the power
print(2 ** 3)  # 2 * 2 * 2 = 8
print(2 ** 4)  # 2 * 2 * 2 * 2 = 16

# Roots of a number
print(25 ** 0.5)
print(16 ** 0.5)

# Order of Operations in Python
print(5 + 10 * 10 + 3)  # 108

# We can use parenthesis to specify orders
print((5 + 10) * (10 + 3))

# type() function returns the data type of any variable
print(type(10))  # int
print(type(6.7))  # float
print(type("25"))  # str
print(type(True))  # bool
print(type(False))

# Variable Assignment
x = 3
print(x)

print(x + x)

x = 81
print(x)

y = x
print(y)

a = 5
print(type(a))

# Type Conversion | Casting
print(1 + 2.0)
print(int(2.9))  # int()
print(int(-3.5))
print(float(66))  # float()
